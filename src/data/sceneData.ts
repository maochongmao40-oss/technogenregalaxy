function platformLinks(query: string): PlaybackOption[] {
  const encoded = encodeURIComponent(query);
  return [
    {
      group: 'platform-embed',
      provider: 'spotify',
      label: 'Spotify embed',
      status: 'confirmed-search-url',
      authorization: 'platform-managed',
      authorizationNote: 'Use the platform search/player; do not download or self-host recordings.',
      url: `https://open.spotify.com/search/${encoded}`,
    },
    {
      group: 'platform-embed',
      provider: 'youtube',
      label: 'YouTube embed',
      status: 'confirmed-search-url',
      authorization: 'platform-managed',
      authorizationNote: 'Use YouTube search or embedded player behavior and keep playback platform-managed.',
      url: `https://www.youtube.com/results?search_query=${encoded}`,
    },
  ];
}

function node(
  id: SceneNodeId,
  name: string,
  type: SceneNode['type'],
  era: string,
  region: string,
  summary: string,
  historicalRole: string,
  soundSignature: string[],
  relatedGenres: string[],
  keyPlaces: string[],
  keyArtists: string[],
  position: SceneNode['position'],
  color: string,
  accent: string,
): SceneNode {
  return {
    id,
    name,
    type,
    era,
    region,
    summary,
    historicalRole,
    soundSignature,
    relatedGenres,
    keyPlaces,
    keyArtists,
    links: platformLinks(`${name} techno electronic music`),
    position,
    color,
    accent,
  };
}

